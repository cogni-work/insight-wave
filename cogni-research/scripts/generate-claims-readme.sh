  echo "{\"success\": false, \"error\": \"Claims directory not found: $claims_dir\"}" >&2
  exit 2
fi

# ============================================================================
# HELPER FUNCTIONS
# ============================================================================

# Extract YAML frontmatter from markdown file
extract_frontmatter() {
  local file="$1"
  sed -n '/^---$/,/^---$/p' "$file" | sed '1d;$d'
}

# Extract value from YAML frontmatter (simple key: value parsing)
get_frontmatter_value() {
  local file="$1"
  local key="$2"
  local result
  result="$(extract_frontmatter "$file" | grep "^${key}:" | sed "s/^${key}:[[:space:]]*//" | sed 's/^"//' | sed 's/"$//' || true)"
  echo "$result"
}

# Sanitize text for Mermaid mindmap nodes
sanitize_mindmap_label() {
  echo "$1" | sed 's/([^)]*)//g' | sed 's/[:\[\]"<>]//g' | sed 's/  */ /g' | sed 's/^ *//' | sed 's/ *$//'
}

# Generate wikilink using utility script or fallback
generate_wikilink() {
  local entity_dir="$1"
  local filename="$2"
  local display_name="${3:-}"

  local path="${entity_dir}/${DATA_SUBDIR}/${filename}"
  local fallback_link
  if [[ -n "$display_name" ]]; then
    fallback_link="[[$path|$display_name]]"
  else
    fallback_link="[[$path]]"
  fi

  if [[ -x "$WIKILINK_SCRIPT" ]]; then
    local wikilink_result
    if [[ -n "$display_name" ]]; then
      wikilink_result="$(bash "$WIKILINK_SCRIPT" --project-path "$project_path" --entity-dir "$entity_dir" --filename "$filename" --display-name "$display_name" 2>/dev/null || true)"
    else
      wikilink_result="$(bash "$WIKILINK_SCRIPT" --project-path "$project_path" --entity-dir "$entity_dir" --filename "$filename" 2>/dev/null || true)"
    fi

    if [[ -n "$wikilink_result" ]] && echo "$wikilink_result" | jq -e . >/dev/null 2>&1; then
      echo "$wikilink_result" | jq -r '.data.wikilink'
      return
    fi
  fi

  echo "$fallback_link"
}

# ============================================================================
# COLLECT CLAIM FILES
# ============================================================================

claim_files=()
while IFS= read -r -d '' file; do
  claim_files+=("$file")
done < <(find "$claims_dir" -maxdepth 1 -name "claim-*.md" -type f -print0 2>/dev/null | sort -z)

claim_count="${#claim_files[@]}"

if [[ "$claim_count" -eq 0 ]]; then
  echo "{\"success\": false, \"error\": \"No claim files found in $claims_dir\"}" >&2
  exit 3
fi

# ============================================================================
# PARSE CLAIM METADATA (bash 3.2 compatible - no associative arrays)
# ============================================================================

# Track statistics
confidence_sum=0
confidence_count=0
high_confidence_count=0
moderate_confidence_count=0
low_confidence_count=0
flagged_count=0

# Use temp files for dimension grouping (bash 3.2 compatible)
TEMP_DIR="${TMPDIR:-/tmp}/claims-readme-$$"
mkdir -p "$TEMP_DIR"
trap 'rm -rf "$TEMP_DIR"' EXIT

# Track all dimension slugs we've seen
all_dimension_slugs=""

for claim_file in "${claim_files[@]}"; do
  claim_filename="$(basename "$claim_file" .md)"

  # Extract frontmatter values
  claim_text="$(get_frontmatter_value "$claim_file" "claim")"
  if [[ -z "$claim_text" ]]; then
    claim_text="$(get_frontmatter_value "$claim_file" "dc:title")"
  fi
  if [[ -z "$claim_text" ]]; then
    claim_text="$(get_frontmatter_value "$claim_file" "title")"
  fi

  confidence="$(get_frontmatter_value "$claim_file" "confidence")"
  if [[ -z "$confidence" ]]; then
    confidence="$(get_frontmatter_value "$claim_file" "confidence_score")"
  fi

  finding_ref="$(get_frontmatter_value "$claim_file" "finding_ref")"
  if [[ -z "$finding_ref" ]]; then
    finding_ref="$(get_frontmatter_value "$claim_file" "finding_id")"
  fi

  flagged="$(get_frontmatter_value "$claim_file" "flagged_for_review")"
  dimension_ref="$(get_frontmatter_value "$claim_file" "dimension_ref")"

  # Calculate confidence statistics
  if [[ -n "$confidence" ]] && [[ "$confidence" =~ ^[0-9]*\.?[0-9]+$ ]]; then
    # Use bc for floating point arithmetic
    confidence_sum=$(echo "$confidence_sum + $confidence" | bc)
    confidence_count=$((confidence_count + 1))

    # Categorize confidence level
    is_high=$(echo "$confidence > 0.8" | bc)
    is_moderate=$(echo "$confidence >= 0.6" | bc)

    if [[ "$is_high" -eq 1 ]]; then
      high_confidence_count=$((high_confidence_count + 1))
    elif [[ "$is_moderate" -eq 1 ]]; then
      moderate_confidence_count=$((moderate_confidence_count + 1))
    else
      low_confidence_count=$((low_confidence_count + 1))
    fi
  fi

  # Count flagged claims
  if [[ "$flagged" == "true" ]] || [[ "$flagged" == "yes" ]]; then
    flagged_count=$((flagged_count + 1))
  fi

  # Extract dimension slug from reference
  dimension_slug=""
  if [[ -n "$dimension_ref" ]]; then
    dimension_slug="$(echo "$dimension_ref" | grep -o 'dimension-[a-z0-9-]*' | head -1)"
  fi

  if [[ -z "$dimension_slug" ]]; then
    dimension_slug="uncategorized"
  fi

  # Track dimension if new
  if ! echo "$all_dimension_slugs" | grep -q "|${dimension_slug}|"; then
    all_dimension_slugs="${all_dimension_slugs}|${dimension_slug}|"
  fi

  # Fallback claim text if empty
  if [[ -z "$claim_text" ]]; then
    claim_text="$claim_filename"
  fi

  # Append claim data to temp file
  echo "${claim_filename}|${claim_text}|${confidence}|${finding_ref}|${dimension_slug}" >> "$TEMP_DIR/claims_all"
done

# Calculate average confidence
avg_confidence="0.00"
if [[ "$confidence_count" -gt 0 ]]; then
  avg_confidence=$(echo "scale=2; $confidence_sum / $confidence_count" | bc)
fi

