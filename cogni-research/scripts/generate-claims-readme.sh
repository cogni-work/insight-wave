LABEL_CLAIMS="$(get_translation "Claims")"
LABEL_STATISTICS="$(get_translation "Statistics")"
LABEL_MINDMAP="$(get_translation "Mindmap")"
LABEL_ENTITY_INDEX="$(get_translation "Entity Index")"
LABEL_PROVENANCE="$(get_translation "Provenance Chain")"
LABEL_GENERATED_BY="$(get_translation "Generated by")"
LABEL_METRIC="$(get_translation "Metric")"
LABEL_VALUE="$(get_translation "Value")"
LABEL_CLAIM="$(get_translation "Claim")"
LABEL_FINDING="$(get_translation "Finding")"
LABEL_CONFIDENCE="$(get_translation "Confidence")"
LABEL_LINK="$(get_translation "Link")"
LABEL_TOTAL_CLAIMS="$(get_translation "Total Claims")"
LABEL_AVG_CONFIDENCE="$(get_translation "Average Confidence")"
LABEL_FLAGGED="$(get_translation "Flagged for Review")"
LABEL_HIGH_CONFIDENCE="$(get_translation "High Confidence")"
LABEL_MODERATE_CONFIDENCE="$(get_translation "Moderate Confidence")"
LABEL_LOW_CONFIDENCE="$(get_translation "Low Confidence")"
LABEL_CONFIDENCE_DIST="$(get_translation "Confidence Distribution")"
LABEL_TIER="$(get_translation "Tier")"
LABEL_RANGE="$(get_translation "Range")"
LABEL_COUNT="$(get_translation "Count")"
LABEL_CONTAINS="$(get_translation "This directory contains")"
LABEL_EXTRACTED="$(get_translation "verified claims extracted from")"
LABEL_RESEARCH_FINDINGS="$(get_translation "research findings")"

# Build Mermaid mindmap by dimension
mindmap_content=""
entity_index=""

# Group claims by dimension
current_dimension=""
while IFS='|' read -r c_filename c_text c_confidence c_finding c_dimension; do
  if [[ -n "$c_filename" ]]; then
    # Add dimension node if new
    if ! [[ "$c_dimension" == "$current_dimension" ]]; then
      dimension_label="$(sanitize_mindmap_label "$c_dimension" | sed 's/^dimension-//' | cut -c1-30)"
      dimension_label="$(echo "$dimension_label" | sed 's/-/ /g' | awk '{for(i=1;i<=NF;i++) $i=toupper(substr($i,1,1)) substr($i,2)}1')"
      mindmap_content+="    $dimension_label"$'\n'
      current_dimension="$c_dimension"
    fi

    # Add claim as child node
    c_text_short="$(sanitize_mindmap_label "$c_text" | cut -c1-35)"
    mindmap_content+="      $c_text_short"$'\n'

    # Generate wikilink for entity index
    claim_wikilink="$(generate_wikilink "10-claims" "$c_filename")"

    # Format confidence for display
    c_confidence_display="${c_confidence:-N/A}"

    entity_index+="| $claim_wikilink | $c_confidence_display |"$'\n'
  fi
done < <(sort -t'|' -k5 "$TEMP_DIR/claims_all" 2>/dev/null || true)

# ============================================================================
# WRITE README FILE
# ============================================================================

readme_path="$claims_base_dir/README.md"

cat > "$readme_path" <<EOF
---
title: "${LABEL_VERIFIED_CLAIMS} Overview"
generated_by: fact-checker
generated_at: $timestamp
claim_count: $claim_count
avg_confidence: $avg_confidence
high_confidence_count: $high_confidence_count
moderate_confidence_count: $moderate_confidence_count
low_confidence_count: $low_confidence_count
flagged_count: $flagged_count
project_language: $project_language
---

# $LABEL_VERIFIED_CLAIMS

$LABEL_CONTAINS $LABEL_EXTRACTED $LABEL_RESEARCH_FINDINGS.

## $LABEL_PROVENANCE

\`\`\`mermaid
mindmap
  root(($LABEL_CLAIMS))
$mindmap_content\`\`\`

## $LABEL_STATISTICS

| $LABEL_METRIC | $LABEL_VALUE |
|--------|-------|
| $LABEL_TOTAL_CLAIMS | $claim_count |
| $LABEL_AVG_CONFIDENCE | $avg_confidence |
| $LABEL_FLAGGED | $flagged_count |

## $LABEL_CONFIDENCE_DIST

| $LABEL_TIER | $LABEL_RANGE | $LABEL_COUNT |
|------|-------|-------|
| $LABEL_HIGH_CONFIDENCE | >0.8 | $high_confidence_count |
| $LABEL_MODERATE_CONFIDENCE | 0.6-0.8 | $moderate_confidence_count |
| $LABEL_LOW_CONFIDENCE | <0.6 | $low_confidence_count |

## $LABEL_ENTITY_INDEX

| $LABEL_CLAIM | $LABEL_CONFIDENCE |
|-------|----------|
$entity_index
---

*$LABEL_GENERATED_BY fact-checker*
EOF

# ============================================================================
# OUTPUT RESULTS
# ============================================================================

readme_created="false"
if [[ -f "$readme_path" ]]; then
  readme_created="true"
fi

if [[ "$json_output" == "true" ]]; then
  cat <<EOF
{
  "success": true,
  "data": {
    "readme_path": "$readme_path",
    "readme_created": $readme_created
  },
  "stats": {
    "claim_count": $claim_count,
    "avg_confidence": $avg_confidence,
    "high_confidence_count": $high_confidence_count,
    "moderate_confidence_count": $moderate_confidence_count,
    "low_confidence_count": $low_confidence_count,
    "flagged_count": $flagged_count
  }
}
EOF
else
  echo "README created: $readme_path"
  echo "Claims: $claim_count, Avg Confidence: $avg_confidence, Flagged: $flagged_count"
fi

exit 0
