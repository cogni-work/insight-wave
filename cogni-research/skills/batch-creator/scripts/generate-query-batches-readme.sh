# ============================================================================
# ARGUMENT PARSING
# ============================================================================

project_path=""
json_output="false"
project_language="en"

while [[ $# -gt 0 ]]; do
  case "$1" in
    --project-path)
      project_path="$2"
      shift 2
      ;;
    --json)
      json_output="true"
      shift
      ;;
    --language)
      project_language="$2"
      shift 2
      ;;
    *)
      echo "{\"success\": false, \"error\": \"Unknown argument: $1\"}" >&2
      exit 2
      ;;
  esac
done

# Validate required arguments
if [[ -z "$project_path" ]]; then
  echo "{\"success\": false, \"error\": \"Required argument: --project-path\"}" >&2
  exit 2
fi

# Validate project path exists
if [[ ! -d "$project_path" ]]; then
  echo "{\"success\": false, \"error\": \"Project path not found: $project_path\"}" >&2
  exit 2
fi

# ============================================================================
# SOURCE CENTRALIZED CONFIG
# ============================================================================

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# Try to source entity-config.sh for DATA_SUBDIR
if [[ -f "${SCRIPT_DIR}/../../../scripts/lib/entity-config.sh" ]]; then
  source "${SCRIPT_DIR}/../../../scripts/lib/entity-config.sh"
  DATA_SUBDIR="$(get_data_subdir)"
else
  DATA_SUBDIR="data"
fi

